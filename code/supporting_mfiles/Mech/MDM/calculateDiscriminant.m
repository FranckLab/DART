function D = calculateDiscriminant(T)

T00 = T{1,1};
T11 = T{2,2};
T22 = T{3,3};
T01 = T{1,2};
T12 = T{2,3};
T02 = T{1,3};

fx = T00.*(T11.^2 - T22.^2 + T01.^2 - T02.^2) + ...
    T11.*(T22.^2 - T00.^2 + T12.^2 - T01.^2) + ...
    T22.*(T00.^2 - T11.^2 + T02.^2 - T12.^2);

fy1 = T12.*( 2*(T12.^2 - T00.^2) - (T02.^2 + T01.^2) + 2*(T11.*T00 + T22.*T00 - T11.*T22)) + ...
    T01.*T02.*(2*T00 - T22 - T11);


fy2 = T02.*( 2*(T02.^2 - T11.^2) - (T01.^2 + T12.^2) + 2*(T22.*T11 + T00.*T11 - T22.*T00)) + ...
    T12.*T01.*(2*T11 - T00 - T22);

fy3 = T01.*( 2*(T01.^2 - T22.^2) - (T12.^2 + T02.^2) + 2*(T00.*T22 + T11.*T22 - T00.*T11)) + ...
    T02.*T12.*(2*T22 - T11 - T00);

fz1 = T12.*(T02.^2 + T01.^2) + T01.*T02.*(T11 - T22);
fz2 = T02.*(T01.^2 + T12.^2) + T12.*T01.*(T22 - T00);
fz3 = T01.*(T12.^2 + T02.^2) + T02.*T12.*(T00 - T11);

D = fx.^2 + fy1.^2 + fy2.^2 + fy3.^2 + 15*fz1.^2 + 15*fz2.^2 + 15*fz3.^2;

end